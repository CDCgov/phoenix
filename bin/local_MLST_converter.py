def parseArgs(args=None):
    """Takes in a taxa file and creates a file with the taxa found"""
    parser = argparse.ArgumentParser(
        description="Script to convert taxonomy to correctly formatted mlst database name(s) to pull from pubmlst"
    )
    parser.add_argument(
        "-G", "--genus", dest="genus", required=False, help="genus of taxonomy"
    )
    parser.add_argument(
        "-s", "--species", dest="species", required=False, help="species of taxonomy"
    )
    parser.add_argument(
        "--version", action="version", version=get_version()
    )  # Add an argument to display the version
    return parser.parse_args()


